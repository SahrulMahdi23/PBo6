 package laptop;

 public class MacBook implements Laptop {
 
     public void powerOn() {
         System.out.println("MacBook menyala dengan elegan.");
     }
 
     public void powerOff() {
         System.out.println("MacBook dimatikan.");
     }
 
     public void volumeUp() {
         System.out.println("Volume MacBook bertambah.");
     }
 
     public void volumeDown() {
         System.out.println("Volume MacBook berkurang.");
     }
 }
 
